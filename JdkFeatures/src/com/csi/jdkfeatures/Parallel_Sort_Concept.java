package com.csi.jdkfeatures;

public class Parallel_Sort_Concept {

	public static void main(String[] args) {
	int arr[] = {2,8,5,4,3,9};
	System.out.println("\n Before Sort:");
	for(int i : arr){
		System.out.println(i);
	}
	System.out.println("\n After Sort:");
	for(int i : arr){
		System.out.println(i);
	}


	}

}
